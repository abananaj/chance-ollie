/**
 * tokens.js
 * Shared token extraction from theme.json settings and SCSS variables.
 *
 * TOKEN_CATEGORIES defines every named preset array in theme.json that has a
 * slug+value pair. Each entry maps:
 *   - jsonPath      : path through the settings object to the array
 *   - slugKey       : the property name used as the slug (always "slug")
 *   - valueKey      : the property name that holds the value
 *   - scssPrefix    : prepended to slug when forming the SCSS variable name
 *                     (empty string = match existing ct-variables.scss convention)
 *   - wpCssVarFn    : function(slug) → the CSS custom property WordPress generates
 *
 * NOTE: spacing uses `spacingScale` (auto-generated by WordPress at runtime).
 * Those preset slugs (20–80) exist only as CSS vars at runtime; they are not
 * enumerated in settings.json, so they are excluded from sync. They appear in
 * the diff report as a notice rather than conflicts.
 */

// ─────────────────────────────────────────────────────────────────────────────
// Token category definitions
// ─────────────────────────────────────────────────────────────────────────────

